            <!-- Land Verification Instructions -->
            <h3>Land Verification:</h3>
            <p>
                Land verification is the process of authenticating the legal ownership and status of a property.
                Ensure that you have the following documents and follow the necessary steps:
            </p>
            <ul>
                <li>
                    <strong>Title Deed or Ownership Documents:</strong> Critical to establishing ownership and legal
                    status of the property. Ensure the document is genuine and up to date.
                </li>
                <li>
                    <strong>Survey and Subdivision Plans:</strong> These provide detailed measurements,
                    descriptions, and the boundaries of the land.
                </li>
                <li>
                    <strong>Land/Property Tax Records:</strong> Confirm that all taxes related to the property have
                    been paid, ensuring no outstanding liabilities exist.
                </li>
                <li>
                    <strong>Encumbrance Certificate:</strong> This document verifies that there are no mortgages,
                    debts, or legal claims tied to the land.
                </li>
                <li>
                    <strong>Possession Certificate:</strong> A certificate confirming that the individual or entity
                    claiming ownership is in physical possession of the land.
                </li>
            </ul>

            <!-- Land Regularization Instructions -->
            <h3>Land Regularization:</h3>
            <p>
                Land regularization involves aligning informal land ownership with formal legal frameworks. This
                process ensures the property complies with local laws and regulations. The following documents are
                typically required:
            </p>
            <ul>
                <li>
                    <strong>Application Form:</strong> Complete the official application form provided by the
                    appropriate land authorities.
                </li>
                <li>
                    <strong>Identification Card:</strong> A government-issued ID proving the identity of the
                    applicant (e.g., National ID, Passport, or Driver’s License).
                </li>
                <li>
                    <strong>Original Title Deed or Ownership Documents:</strong> Legal documents that confirm the
                    applicant’s ownership of the land.
                </li>
                <li>
                    <strong>Survey Report:</strong> A detailed survey report of the property that includes its
                    boundaries and geographical location.
                </li>
                <li>
                    <strong>Non-Agricultural Land Certificate:</strong> In some cases, a certificate confirming that
                    the land is designated as non-agricultural may be necessary.
                </li>
                <li>
                    <strong>Non-Objection Certificate (NOC):</strong> A document from local authorities stating
                    there are no objections to the proposed regularization.
                </li>
                <li>
                    <strong>Affidavit:</strong> A sworn statement declaring valid ownership of the land and
                    confirming the accuracy of the information provided.
                </li>
                <li>
                    <strong>Proof of Land Use:</strong> If the land use has changed (e.g., from residential to
                    commercial), provide evidence or approval for the change.
                </li>
                <li>
                    <strong>Site Photographs:</strong> Recent photographs of the property to support the
                    regularization application.
                </li>
                <li>
                    <strong>Fee Payment Receipts:</strong> Proof of payment for any fees associated with the
                    regularization process.
                </li>
            </ul>

            <!-- Additional Instructions -->
            <h3>Additional Instructions:</h3>
            <p>
                Before submitting your application, ensure the following:
            </p>
            <ul>
                <li>Cross-check all submitted documents for accuracy and completeness.</li>
                <li>Verify that all approvals and permits (if required) are obtained from the relevant authorities.
                </li>
                <li>Consult a qualified legal professional for guidance if you are unsure about any of the
                    requirements.</li>
            </ul>
