        <h2>Key Steps and Considerations in Land Verification on E-Properties</h2>
        <p>Below are the key steps and considerations involved in land verification on e-properties:</p>
        <ul>
            <li>
                <strong>Survey and Identification:</strong> Begin by obtaining a copy of the survey plan for the
                land.
                The survey plan will provide details on the land's boundaries, size, and shape. This is crucial for
                verifying
                the physical attributes of the land.
            </li>
            <li>
                <strong>Title Search:</strong> Conduct a title search at the appropriate land registry or government
                office to
                verify the ownership and title status of the land. This involves checking the land records to
                confirm that the seller
                has the legal right to transfer ownership.
            </li>
            <li>
                <strong>Search for Encumbrances:</strong> Check for any encumbrances on the land, such as mortgages,
                liens, or legal disputes.
                This can be done through a search at the land registry or by obtaining a land information
                certificate.
            </li>
            <li>
                <strong>Verification of Ownership Documents:</strong> Examine the seller’s documents, including the
                Certificate of Occupancy (C of O),
                Deed of Assignment, or other relevant titles. Ensure that the documents are genuine, up-to-date, and
                consistent with the information
                recorded at the land registry.
            </li>
            <li>
                <strong>Physical Inspection:</strong> Physically visit the land to verify its existence, boundaries,
                and suitability for the intended use.
                This step helps to confirm that the physical features align with the information provided in the
                documents.
            </li>
            <li>
                <strong>Community and Government Approvals:</strong> Check if the land has the necessary approvals
                from the local community
                and relevant government authorities for the intended use (e.g., residential, commercial,
                agricultural). This may involve
                obtaining planning permits and other clearances.
            </li>
            <li>
                <strong>Verification of Survey Coordinates:</strong> Cross-check the survey coordinates and ensure
                that they match the details
                provided in the survey plan. This helps prevent disputes related to boundary discrepancies.
            </li>
            <li>
                <strong>Environmental Impact Assessment (EIA):</strong> For certain types of land use, especially
                those with potential
                environmental impact, conduct an EIA to ensure compliance with environmental regulations.
            </li>
            <li>
                <strong>Legal Advice:</strong> Seek legal advice from a qualified property lawyer or conveyancer to
                ensure that all aspects
                of the land transaction comply with Nigerian land laws and regulations.
            </li>
            <li>
                <strong>Payment Verification:</strong> If any payments are made during the transaction, ensure that
                they are properly documented,
                and receipts are obtained. This is crucial for future reference and legal purposes.
            </li>
            <li>
                <strong>Neighborhood Survey:</strong> This involves going to the location of the property you intend
                to rent or acquire to ask
                questions and get important information from the neighbors. This will help you find out whether you
                are dealing with a real
                owner of the property.
            </li>
            <li>
                <strong>Neighborhood Valuation:</strong> You are advised to check out the prices of the properties
                located within that neighborhood,
                to give you a clue of the value of the property.
            </li>
            <li>
                <strong>Photo Identification:</strong> There is a need to have a picture identification of the owner
                of the property or the property itself.
                In case the owner of the property is available, there is a need to have a meeting with the owner of
                the property.
            </li>
        </ul>
